package utilities;

public class Environments {

    private String token;
    private String url;
    private String username;
    private String password;


    public String getToken() {
        return token;
    }

    public String getUsername() {
        return username;
    }

    public String getUrl() {
        return url;
    }

    public String getPassword() {
        return password;
    }
}
